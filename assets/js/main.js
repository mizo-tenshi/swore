// ================================================
// LANGUAGE SWITCHER
// ================================================
function applyLang(lang) {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (T[lang][key] !== undefined) {
      el.innerHTML = T[lang][key];
    }
  });
  document.documentElement.lang = lang;
  document.title = lang === 'en'
    ? 'SWORE | Rare Loopwheel Machines with a Century of History'
    : 'SWORE | 百年の歴史を持つ希少な吊り編み機。';
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
  localStorage.setItem('swore-lang', lang);
}

document.querySelectorAll('.lang-btn').forEach(btn => {
  btn.addEventListener('click', () => applyLang(btn.dataset.lang));
});

const savedLang = localStorage.getItem('swore-lang') || 'ja';
if (savedLang === 'en') applyLang('en');

// ================================================
// SCROLL REVEAL
// ================================================
const io = new IntersectionObserver(entries => {
  entries.forEach(e => {
    if (e.isIntersecting) { e.target.classList.add('on'); }
  });
}, { threshold: 0.1 });

document.querySelectorAll('.r').forEach((el) => {
  const siblings = [...el.parentElement.querySelectorAll('.r')];
  el.style.transitionDelay = (siblings.indexOf(el) * 0.12) + 's';
  io.observe(el);
});

// ================================================
// NAV SCROLL
// ================================================
const nav = document.querySelector('nav');
window.addEventListener('scroll', () => {
  nav.classList.toggle('scrolled', window.scrollY > 60);
}, { passive: true });
